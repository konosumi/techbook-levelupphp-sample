<?php
/**
 * 配列
 */

/** (PHP5.4) 配列の短縮構文 **/
$array = array(1, 2, 3); // 通常の配列定義
$array = [1, 2, 3]; // (PHP5.4)配列定義の短縮構文

/** 添字配列の基本形(要素名を付けない) **/
$a = ['a', 'b'];
// array(2) {[0]=> string(1) "a" [1]=> string(1) "b"}
var_dump($a);

/** 連想配列の基本形(要素名を付ける) **/
$b = [
    "a" => "b",
    "c" => "d",
];
// array(2) {["a"]=> string(1) "b" ["c"]=> string(1) "d"}
var_dump($b);
